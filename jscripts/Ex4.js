let cadena = "123"
let numero = 456        

let resultado = cadena + numero

console.log("Resultado: " + resultado)

/* Al sumar una cadena con un número, JavaScript convierte el número a una cadena y 
concatena ambos valores. En este caso, la variable 'numero' (456) se convierte en 
cadena y se une a 'cadena' ("123"), resultando en "123456" en lugar de realizar 
una operación matemática de suma. */